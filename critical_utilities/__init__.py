class CriticalInfo:

    def __init__(self,
                 no_critical,
                 no_super_critical,
                 critical_degrees,
                 critical_to_non_critical_degrees,
                 non_critical_to_critical_degrees,
                 non_critical_degrees,
                 degrees):

         self.no_critical = no_critical
         self.no_super_critical = no_super_critical
         self.critical_degrees = critical_degrees
         self.critical_to_non_critical_degrees = critical_to_non_critical_degrees
         self.non_critical_to_critical_degrees = non_critical_to_critical_degrees
         self.non_critical_degrees = non_critical_degrees
         self.degrees = degrees
